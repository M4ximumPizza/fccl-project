package core.combine.classes.java.lang.annotations;

/**
 * An annotation used to designate a class as unusable due to it being either
 * unfinished or non-functional, signifying that it's still in active development.
 *
 * It's important to distinguish this annotation from {@link Deprecated}, which
 * flags a class as deprecated, indicating that while it is still implemented,
 * its usage is discouraged.
 *
 * This annotation specifically highlights classes that are either incomplete or
 * non-operational, emphasizing that their usage is not recommended.
 *
 * @since 1.0
 * @author Logan Abernathy
 */
public @interface Unuseable {
}
